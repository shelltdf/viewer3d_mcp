/** 无本地覆盖时：系统语言为中文（zh*）→ 中文，否则英文 */
export function detectDefaultLocale() {
  if (typeof navigator === 'undefined') return 'en'
  const nav = (navigator.language || '').toLowerCase()
  return nav.startsWith('zh') ? 'zh' : 'en'
}

