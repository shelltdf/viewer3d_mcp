export function useUiLocale() {
  let initial = detectDefaultLocale()
  try {
    const s = localStorage.getItem(STORAGE_KEY)
    if (s === 'zh' || s === 'en') initial = s
  } catch {
    /* ignore */
  }

  const locale = ref(/** @type {'zh' | 'en'} */ (initial))

  watch(locale, (v) => {
    try {
      localStorage.setItem(STORAGE_KEY, v)
    } catch {
      /* ignore */
    }
  })

  /** @param {keyof typeof MESSAGES['en']} key */
  function t(key) {
    const pack = MESSAGES[locale.value] || MESSAGES.en
    return pack[key] ?? MESSAGES.en[key] ?? String(key)
  }

  /** @param {'zh' | 'en'} v */
  function setLocale(v) {
    if (v === 'zh' || v === 'en') locale.value = v
  }

  return { locale, t, setLocale, messages: MESSAGES }
}
